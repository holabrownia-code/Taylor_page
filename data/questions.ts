export function getRandomQuestions(era: string, difficulty: string, count = 10): Question[] {
  const eraQuestions = questions[era] || []
  const filteredQuestions = eraQuestions.filter((q) => q.difficulty === difficulty)

  // Shuffle array and return first 'count' questions
  const shuffled = [...filteredQuestions].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}
